const dicts: Record<Lang, Dict> = { en, hi, te };

const STORAGE_KEY = "lc:lang";

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  tStatus: (status: string | null | undefined) => string;
  tService: (slug: string | null | undefined, fallback?: string | null) => string;
  timeAgo: (iso: string) => string;
};

const I18nContext = createContext<Ctx | null>(null);

function timeAgoFor(iso: string, t: (k: string) => string): string {
  const diff = Date.now() - new Date(iso).getTime();
  const m = Math.floor(diff / 60000);
  if (m < 1) return t("common.justNow");
  if (m < 60) return `${m} ${t("common.minAgo")}`;
  const h = Math.floor(m / 60);
  if (h < 24) return `${h} ${t("common.hrAgo")}`;
  const d = Math.floor(h / 24);
  if (d < 7) return `${d} ${t("common.dayAgo")}`;
  return new Date(iso).toLocaleDateString();
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    if (typeof window === "undefined") return;
    const saved = localStorage.getItem(STORAGE_KEY) as Lang | null;
    if (saved && dicts[saved]) setLangState(saved);
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") {
      localStorage.setItem(STORAGE_KEY, l);
      document.documentElement.lang = l;
    }
  }, []);

  const t = useCallback(
    (key: string) => dicts[lang][key] ?? dicts.en[key] ?? key,
    [lang],
  );

  const tStatus = useCallback(
    (s: string | null | undefined) => {
      if (!s) return "";
      const key = `status.${s}`;
      const v = dicts[lang][key] ?? dicts.en[key];
      return v ?? s;
    },
    [lang],
  );

  const tService = useCallback(
    (slug: string | null | undefined, fallback?: string | null) => {
      if (!slug) return fallback ?? "";
      const key = `service.${slug}`;
      const v = dicts[lang][key] ?? dicts.en[key];
      return v ?? fallback ?? slug;
    },
    [lang],
  );

  const timeAgo = useCallback((iso: string) => timeAgoFor(iso, t), [t]);

  const value = useMemo(
    () => ({ lang, setLang, t, tStatus, tService, timeAgo }),
    [lang, setLang, t, tStatus, tService, timeAgo],
  );
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): Ctx {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    const fallbackT = (k: string) => dicts.en[k] ?? k;
    return {
      lang: "en",
      setLang: () => {},
      t: fallbackT,
      tStatus: (s) => (s ? dicts.en[`status.${s}`] ?? s : ""),
      tService: (slug, fb) => (slug ? dicts.en[`service.${slug}`] ?? fb ?? slug : fb ?? ""),
      timeAgo: (iso) => timeAgoFor(iso, fallbackT),
    };
  }
  return ctx;
}
